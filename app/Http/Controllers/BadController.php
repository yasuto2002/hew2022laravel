     */
    public function store(Request $request)
    {
        DB::beginTransaction();
        try{
            $Uid = DB::table('users')->select('id')->where('mail_address',$request->mail_address)->first();
            DB::table('goods')->where('id',$request->id)->where('users_id',$Uid->id)->delete();
            $count = DB::table('goods')->where('id',$request->id)->count();
            DB::commit();
            return ['status'=>true,'count' => $count];
        }catch(\Exception $e){
            DB::commit();
            return ['status'=>false];
        }
    }

