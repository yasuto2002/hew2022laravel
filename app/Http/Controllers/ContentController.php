     */
    public function store(Request $request)
    {
        DB::beginTransaction();
        try{
            $param=[
                'name' => $request->name,
                'email_address' => $request->mail_address,
                'contact_detail' => $request->text,
            ];
            DB::table('contact_users')->insert($param);
            DB::commit();
            $flg = ['state'=>true];
            return $flg;
        }catch(\Exception $e){
            $flg = ['state'=>false];
            return $flg;
        }
    }

